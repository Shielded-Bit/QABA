export function getPropertyLocations(state) {
  return propertyLocationIndex[state.toLowerCase()] || [];
}
export function getPropertyLocationSuggestions(input) {
  const lowerInput = input.toLowerCase();
  const suggestions = [];

  for (const state in propertyLocationIndex) {
    if (state.toLowerCase().includes(lowerInput)) {
      suggestions.push(state);
    } else {
      propertyLocationIndex[state].forEach(location => {
        if (location.toLowerCase().includes(lowerInput)) {
          suggestions.push(location);
        }
      });
    }
  }

  return [...new Set(suggestions)]; // Remove duplicates
}
export function getPropertyLocationByState(state) {
  return propertyLocationIndex[state.toLowerCase()] || [];
}
export function getAllPropertyLocations() {
  const allLocations = [];
  for (const state in propertyLocationIndex) {
    allLocations.push(...propertyLocationIndex[state]);
  }
  return [...new Set(allLocations)]; // Remove duplicates
}